public class Main {					
	public static void main(String args[]) {
		schreiben(5);
		schreiben("Hallo Welt");
		schreiben(true);
	}
	public static void schreiben(int input) {
		System.out.println(input);
	}
	public static void schreiben(String input) {
		System.out.println(input);
	}
	public static void schreiben(boolean input) {
		System.out.println(input);
	}
}