public class Main {
	public static void main(String args[]) {
		int i = 10;
		int ii = 20;
		String s = "Hallo";
		float f = 10.5f;
		
		System.out.printf("i = %d %n", i);
		System.out.printf("s = %s %n", s);
		System.out.printf("f = %f %n", f);
		System.out.printf("%d + %d = %d", i, ii, (i + ii));
	}
}